            <h1 class="title">Tenormin (Atenolol)</h1>
            <h2 class="subtitle">What Is Tenormin?</h2>
            <p class="content">Tenormin is a beta-blocker indicated for the treatment of hypertension, to lower blood
                pressure. Lowering blood pressure lowers the risk of fatal and non-fatal cardiovascular events,
                primarily strokes and myocardial infarctions (heart attacks).</p>
            <h2 class="subtitle">Side Effects</h2>
            <div class="side-effects">
                <p class="side-effects-title">Common Side Effects</p>
                <p class="side-effects-description">Side effects of Tenormin include: dizziness, nausea, slow heart
                    rate, fatigue/tiredness, cold extremities, depression, shortness of breath, lethargy,
                    lightheadedness, leg pain, and spinning sensation (vertigo).</p>
                <p class="side-effects-title">Serious Side Effects</p>
                <p class="side-effects-description">Seek medical care or call 911 at once if you have the following
                    serious side effects: Serious eye symptoms such as sudden vision loss, blurred vision, tunnel
                    vision, eye pain or swelling, or seeing halos around lights; Serious heart symptoms such as fast,
                    irregular, or pounding heartbeats; fluttering in your chest; shortness of breath; and sudden
                    dizziness, lightheadedness, or passing out; Severe headache, confusion, slurred speech, arm or leg
                    weakness, trouble walking, loss of coordination, feeling unsteady, very stiff muscles, high fever,
                    profuse sweating, or tremors.</p>
                <p class="side-effects-title">Note</p>
                <p class="side-effects-description">This document does not contain all possible side effects and others
                    may occur. Check with your physician for additional information about side effects.</p>
